package ControlStructures;

public class ApplicationSwtichCase {

	public static void main(String[] args) {

		int number = 11;
		
		switch(number) {
		case 5:
			System.out.println("Numero igual a: " + number);
			break;
		case 10:
			System.out.println("Numero igual a: " + number);
			break;
		case 15:
			System.out.println("Numero igual a: " + number);
			break;
		default:
			System.out.println("Numero não encontrado.");
			break;
			
		}
		
	}

}
